public class Day09_SumInteger {

    // Type code to find the sum of the integers from 12 to 15
    // 12 ile 15 arasındaki tam sayıların toplamını bulunuz

    public static void main(String[] args){


        int sum = 0;
        for(int i=12; i<15; i++) {
            sum = sum + i;
        }
        System.out.println(sum);
    }
        }

